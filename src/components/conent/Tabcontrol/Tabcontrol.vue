<template>
  <div class="tab-control">
      <div v-for="(item,index) in titles" class="tab-control-item" :class="{active: index === currentindex}" @click="itemclick(index)">
          <span>{{item}}</span>
      </div>
  </div>
</template>

<script>
export default {
    name: 'TabControl',
    props: {
        titles: {
            type: Array,
            default: []
        }
    },
    data() {
        return {
            currentindex: 0
        }
    },
    methods:{
        itemclick(index){
            this.currentindex = index
            this.$emit('tabClick',index)
            
        }
    }
}
</script>

